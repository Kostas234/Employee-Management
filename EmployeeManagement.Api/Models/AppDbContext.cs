namespace EmployeeManagement.Api.Models
{
    public class AppDbContext:DbContext
    {
        // We need an instance of the DbContextOPtions class so we create
        // a constructor with a DbContextOptions instance as an argument named
        // options and we pass this to the the base DBContext class and we do
        // this using the base keyword
        public AppDbContext(DbContextOptions<AppDbContext> options)
        : base(options)
        {
        }

        // We need a DBSet project for each entity in the model so we create
        // one for Employee and one for Department and we will use these to 
        // query and save instances of Employee and Department classes
        // The LINQ queries against the DbSet<TEntity> will be translated into queres against
        // the underlying database
        public DbSet<Employee> Employees { get; set; }
        public DbSet<Department> Departments { get; set; }

        protected override void OnModelCreating(ModelBuilder modelBuilder)
        {
            base.OnModelCreating(modelBuilder);

            //Seed Departments Table
